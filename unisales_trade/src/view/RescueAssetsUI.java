    private void cmbRescueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbRescueActionPerformed
        // TODO add your handling code here:
        if(cmbRescue.getSelectedItem().toString().equals("Total"))
        {
            this.txtQuatity.setEnabled(false);
            this.txtQuatity.setText(String.valueOf(this._assets.getQt_asset()));
            
            this.txtValueRescue.setEnabled(false);
            this.txtValueRescue.setText(this._assets.getVl_amountInvested().toString());
            
        }
        else
        {
            this.txtQuatity.setEnabled(true);
            this.txtQuatity.setText("");
            
            this.txtValueRescue.setEnabled(true);
            this.txtValueRescue.setText("");
        }
    }//GEN-LAST:event_cmbRescueActionPerformed

