    private void backAssets()
    {
        AssetsTradeUI assetsUI = AssetsTradeUI.getInstance(_wallet);
        assetsUI.setEnabled(true);
        assetsUI.loadTable();
        this.dispose();
    }

    private void loadCombo()
    {
        Object[] values = {"-- Selecione --","Total","Parcial"};
        UtilServices.loadCombo(cmbRescue, values);
        //this.cmbRescue = new JComboBox(values);
    }
    
     private void loadComponents(Assets a)
    {
        this.loadCombo();
        this.setLocationRelativeTo(null);
        this._wDAO = new WalletDAO();
        this._aDAO = new AssetsDAO();
        this._nf = NumberFormat.getCurrencyInstance();
        this._assets = a;
        this._wallet = a.getWallet();
        this.lblAsset.setText(a.getNm_asset());
    }   
