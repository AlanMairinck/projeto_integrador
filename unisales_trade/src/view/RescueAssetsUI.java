    private void btnRescueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRescueActionPerformed
        // TODO add your handling code here: -- Selecione --
        try 
        {
           if(cmbRescue.getSelectedItem().toString().equals("-- Selecione --"))
            {
                JOptionPane.showMessageDialog(null,
                            "Selecione o tipo de resgate.\n", //mensagem
                            "Resgate", // titulo da janela
                            JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if(cmbRescue.getSelectedItem().toString().equals("Parcial"))
            {
                if(( txtQuatity.getText().isEmpty() || txtQuatity.getText() == null)
                    || (txtValueRescue.getText().equals("0,00")))
                {
                    JOptionPane.showMessageDialog(null,
                        "Você deve informar a quantidade e valor do resgate.\n"
                                + "Tente novamente.", //mensagem
                        "Ativo", // titulo da janela
                        JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
            }
            if(cmbRescue.getSelectedItem().toString().equals("Total"))
            {
                this._wallet.setAmount_invested(this._wallet.getAmount_invested().subtract(this._assets.getVl_amountInvested()));
                this._wDAO.update(this._wallet);

                this._aDAO.delete(this._assets.getId_asset());
                
            }
            else
            {
                int qtd = Integer.parseInt(txtQuatity.getText());
                String cot = this.txtValueRescue.getText();
                cot = cot.replace(".","").replace(",",".");
                BigDecimal cotacao = new BigDecimal(cot);
                BigDecimal montante = cotacao.multiply(BigDecimal.valueOf(qtd));

                if(this._assets.getVl_amountInvested().compareTo(montante) == -1)
                {
                    JOptionPane.showMessageDialog(null,
                        "Montante resgatado maior que o disponível no ativo.\n", //mensagem
                        "Ativo", // titulo da janela
                        JOptionPane.INFORMATION_MESSAGE);
                    return;
                }

                this._assets.setQt_asset(this._assets.getQt_asset()-qtd);
                this._assets.setVl_amountInvested(this._assets.getVl_amountInvested().subtract(montante));

                this._wallet.setAmount_invested(this._wallet.getAmount_invested().subtract(montante));

                this._aDAO.update(this._assets);
                this._wDAO.update(this._wallet);

                JOptionPane.showMessageDialog(null,
                                    "Resgate "+ this._assets.getNm_asset().toUpperCase() + " salvo com sucesso. \n", //mensagem
                                    "Ativo", // titulo da janela
                                    JOptionPane.INFORMATION_MESSAGE);
                
            }  
            } catch (Exception e) {
                Logger.getLogger(InsertAssetsUI.class.getName()).log(Level.SEVERE, null, e);
            }
            this.backAssets();
    }//GEN-LAST:event_btnRescueActionPerformed

